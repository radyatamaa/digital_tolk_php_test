        /**
     * @param $user_id
     * @return array
     */
    public function getUsersJobs($user_id)
    {
        $cuser = User::find($user_id);
    
        if (!$cuser) {
            return ['emergencyJobs' => [], 'noramlJobs' => [], 'cuser' => null, 'usertype' => ''];
        }
    
        $usertype = $cuser->is('customer') ? 'customer' : ($cuser->is('translator') ? 'translator' : '');
    
        $jobs = [];
        if ($usertype === 'customer') {
            $jobs = $cuser->jobs()
                ->with('user.userMeta', 'user.average', 'translatorJobRel.user.average', 'language', 'feedback')
                ->whereIn('status', ['pending', 'assigned', 'started'])
                ->orderBy('due', 'asc')
                ->get();
        } elseif ($usertype === 'translator') {
            $jobs = Job::getTranslatorJobs($cuser->id, 'new')->pluck('jobs')->all();
        }
    
        $emergencyJobs = [];
        $noramlJobs = [];
    
        foreach ($jobs as $jobitem) {
            $targetArray = ($jobitem->immediate === 'yes') ? $emergencyJobs : $noramlJobs;
    
            $targetArray[] = array_merge($jobitem->toArray(), [
                'usercheck' => Job::checkParticularJob($user_id, $jobitem)
            ]);
        }
    
        $noramlJobs = collect($noramlJobs)->sortBy('due')->all();
    
        return ['emergencyJobs' => $emergencyJobs, 'noramlJobs' => $noramlJobs, 'cuser' => $cuser, 'usertype' => $usertype];
    }

    /**
     * @param $user_id
     * @return array
     */
    public function getUsersJobsHistory($user_id, Request $request)
    {
        $pagenum = $request->get('page', "1");
        $cuser = User::find($user_id);
    
        if (!$cuser) {
            return ['emergencyJobs' => [], 'noramlJobs' => [], 'jobs' => null, 'cuser' => null, 'usertype' => '', 'numpages' => 0, 'pagenum' => 0];
        }
    
        $usertype = $cuser->is('customer') ? 'customer' : ($cuser->is('translator') ? 'translator' : '');
    
        if ($usertype === 'customer') {
            $jobs = $cuser->jobs()
                ->with('user.userMeta', 'user.average', 'translatorJobRel.user.average', 'language', 'feedback', 'distance')
                ->whereIn('status', ['completed', 'withdrawbefore24', 'withdrawafter24', 'timedout'])
                ->orderBy('due', 'desc')
                ->paginate(15);
    
            return [
                'emergencyJobs' => [],
                'noramlJobs' => [],
                'jobs' => $jobs,
                'cuser' => $cuser,
                'usertype' => 'customer',
                'numpages' => 0,
                'pagenum' => 0,
            ];
        } elseif ($usertype === 'translator') {
            $jobs_ids = Job::getTranslatorJobsHistoric($cuser->id, 'historic', $pagenum);
            $numpages = ceil($jobs_ids->total() / 15);
    
            return [
                'emergencyJobs' => [],
                'noramlJobs' => $jobs_ids,
                'jobs' => $jobs_ids,
                'cuser' => $cuser,
                'usertype' => 'translator',
                'numpages' => $numpages,
                'pagenum' => $pagenum,
            ];
        }
    
        return ['emergencyJobs' => [], 'noramlJobs' => [], 'jobs' => null, 'cuser' => null, 'usertype' => '', 'numpages' => 0, 'pagenum' => 0];
    }

    /**
     * making user_tags string from users array for creating onesignal notifications
     * @param $users
     * @return string
     */
    private function getUserTagsStringFromArray($users)
    {
        $user_tags = "[";
        $first = true;
        foreach ($users as $oneUser) {
            if ($first) {
                $first = false;
            } else {
                $user_tags .= ',{"operator": "OR"},';
            }
            $user_tags .= '{"key": "email", "relation": "=", "value": "' . strtolower($oneUser->email) . '"}';
        }
        $user_tags .= ']';
        return $user_tags;
    }


